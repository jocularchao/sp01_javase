public class VariableDemo1 {
    public static void main(String[] args) {
        //数据类型 变量名称 = 初始值;
        double money=6.0;
        System.out.println(money);  //6.0


        //收红包
        money+=4.0;
        System.out.println(money);  //10.0

        System.out.println("---------------------------------");

        System.out.println();
        int age=21;
        System.out.println(age);

        age=25;
        System.out.println(age);

    }
}
